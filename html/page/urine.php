			<h2>Пожалуйста, внимательно ознакомьтесь с этими важными правилами перед сбором мочи:</h2>
			<ul>
				<li>Избегайте одновременного употребления большого количества жидкости в день сбора теста. Вместо этого пейте понемногу в течение дня. Общее потребление жидкости в день сбора не должно превышать 3 л.</li>
				<li>Избегайте приема пищевых добавок, содержащих креатин, накануне и в день сбора.</li>
				<li>Не делайте сбор теста, если в моче может быть кровь. Менструирующие женщины не должны сдавать тест во время менструации. </li>
				<li>Женщины в постменопаузе, принимающие ЗГТ (эстроген и прогестерон), должны собирать в дни после пятидневного одновременного приема эстрогена и прогестерона.</li>
				<li>Женщины в постменопаузе, не принимающие гормоны, должны собирать в день с обычным уровнем нагрузки.</li>
				<li>Накануне и в день сбора избегайте употребления бананов, ананасов, авокадо, орехов и орехового масла, алкоголя, никотина и протеиновых коктейлей.</li>
				<li>Если вы выбрали тест на баланс сна (Sleep Balance), избегайте приёма мелатонина или любых снотворных препаратов за 2 ночи до сбора.</li>
				<li>Несоблюдение этих правил может повлиять на ваши результаты или привести к тому, что ваш образец будет признан недействительным.				</li>
			</ul>
