			<h2>1-й забор утром (5-8 утра):</h2>
			<p>Соберите мочу сразу после пробуждения. После сбора вы можете принимать пищевые добавки, лекарства и завтракать как обычно. Для достижения наилучших результатов пейте 50-100 мл воды (или другой жидкости) каждые 30 минут до времени 2-го сбора.</p>
			<h3>2-й забор утром:</h3>
			<p>Соберите мочу через 2-3 часа после предыдущего сбора. Продолжайте пить 50-100 мл жидкости каждые 30 минут до времени 3-го сбора.</p>
			<h3>3-й забор во второй половине дня, до ужина (с 15 до 18 часов):</h3>
			<p>Соберите мочу не ранее, чем через 4 часа после предыдущего сбора. Продолжайте пить 50-100 мл жидкости каждые 30 минут до времени 4-го сбора.</p>
			<h3>4-й забор перед сном (9 вечера-полночь):</h3>
			<p>Соберите мочу не ранее, чем через 4 часа после предыдущего сбора.</p>
